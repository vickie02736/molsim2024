#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main()
{
    
    printf("%f", 5.0+4.5);
    printf("\n");

    printf("%f", 5+4.5);
    printf("\n");

    printf("%d", 5/4);   
    printf("\n");

    printf("%f", 5/4.0);  
    printf("\n");

    int num = 6;
    printf("%d", num);
    printf("\n");

    printf("%f", pow(2,3));
    printf("\n");

    printf("%f", sqrt(36));
    printf("\n");

    printf("%f", ceil(36.543));
    printf("\n");

    printf("%f", floor(36.765));
    printf("\n");


    return 0;
}
